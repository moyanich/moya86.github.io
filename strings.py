# Working with strings

string_exp = "Tomato"
print(string_exp)

# Printing an index
exp_2 = "Dance Moms"
print(exp_2[2])
print("apple"[4])

# String Slicing
exp_3 = "Tomato"

# Slicing a string from the beginning to an end point
print(exp_3[:3])

# Slicing a string from a starting point to an end point
print(exp_3[2:5])

# Slicing a string from a starting point to the end of string
print(exp_3[2:])